    private void txtTagRunnerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTagRunnerActionPerformed
        // TODO add your handling code here:
        //obtener el nombre del corredor seleccionado
        String name = cmbRunner.getSelectedItem().toString();
        //obtener la imagen seleccionada
        File file = fileMulti;
        //etiquetar la imagen con el nombre del corredor
        Multimedia multimedia = new Multimedia(RunnerList.getRunnerByName(name), file);
        //agregar la imagen a la lista de imagenes
        MultimediaList.addMultimedia(multimedia);

        //mensaje de exito
        javax.swing.JOptionPane.showMessageDialog(this, "Imagen etiquetada con éxito", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        //vaciar imagen 
        image.setIcon(null);



    }//GEN-LAST:event_txtTagRunnerActionPerformed

