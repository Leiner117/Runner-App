    private void btnUploadImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadImageActionPerformed
        
        //crear un objeto de la clase JFileChooser
        JFileChooser fc = new JFileChooser();
        //abrir la ventana de dialogo para seleccionar la imagen
        int seleccion = fc.showOpenDialog(this);
        //si el usuario selecciona una imagen
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            //obtener la imagen seleccionada
            File file = fc.getSelectedFile();
            fileMulti = file;
            // ajustar la imagen al tamaño del label
            ImageIcon imageIcon = new ImageIcon(file.getAbsolutePath());
            Image image1 = imageIcon.getImage().getScaledInstance(image.getWidth(), image.getHeight(), Image.SCALE_SMOOTH);
            //mostrar la imagen en el label
            image.setIcon(new ImageIcon(image1));
        }
    }//GEN-LAST:event_btnUploadImageActionPerformed
