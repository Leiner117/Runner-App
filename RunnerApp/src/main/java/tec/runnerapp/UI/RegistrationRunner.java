    private void chargeRunners() {
        cmbRunner.removeAllItems();
        //Runner runner1 = new Runner("Leiner", "leiner@gmail.com", "password", "353335", "A+");
        //RunnerList.addRunner(runner1);
        for (Runner runner : RunnerList.getInstance()) {
            cmbRunner.addItem(runner.getName());
        }
    }
    private void chargeDistances() {
        cmbDistance.removeAllItems();
        cmbDistance.addItem("5K");
        cmbDistance.addItem("10K");
        cmbDistance.addItem("21K");
        cmbDistance.addItem("42K");

    }
    private void chargeLateralization() {
        cmbLateralization.removeAllItems();
        cmbLateralization.addItem("Derecha");
        cmbLateralization.addItem("Izquierda");
    }
    private void chargeCategories() {
        cmbCategory.removeAllItems();
        cmbCategory.addItem("Juvenil");
        cmbCategory.addItem("Libre");
        cmbCategory.addItem("Master");
    }
