    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        // realizar registro del corredor
        //recuperar datos del input
        String name = cmbRunner.getSelectedItem().toString();
        String distance = cmbDistance.getSelectedItem().toString();
        String lateralization = cmbLateralization.getSelectedItem().toString();
        String category = cmbCategory.getSelectedItem().toString();
        String emergencyName = txtEmergencyName.getText();
        String emergencyNum = txtEmergencyNum.getText();
        RunnerList.getRunnerByName(name);
        //crear objeto de registro
        Registration registration = new Registration(distance, lateralization, category, emergencyName, emergencyNum, RunnerList.getRunnerByName(name));
        RegistrationList.addRegistration(registration);
        registration.setParticipantNumber(RegistrationList.getInstance().size());
        
        //mensaje de exito
        javax.swing.JOptionPane.showMessageDialog(this, "Registro del corredor en la carrera exitoso", "Ãxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        //limpiar campos
        txtEmergencyName.setText("");
        txtEmergencyNum.setText("");
        

    }//GEN-LAST:event_btnRegisterActionPerformed

